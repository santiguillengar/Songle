public class IconStyle {

    public final String scale;
    public final String icon;



    public IconStyle(String scale, String icon) {
        this.scale = scale;
        this.icon = icon;
    }

    public String getScale() {
        return scale;
    }

    public String getIcon() {
        return icon;
    }

}
